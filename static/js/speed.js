// If *str* represents a float value, raise an alert with *msg* then
// return true. Otherwise, return false.
function isaFloat(str, msg) {
    var fval = parseFloat(str);
    var ival = parseInt(fval);
    if (fval - ival != 0) {
        alert(msg);
        return(true);
    } else {
        return(false);
    }
}

